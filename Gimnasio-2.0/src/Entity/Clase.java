package Entity;

public class Clase {
	
	int idClase;
	int idSala;
	
	public Clase (int idClase, int idSala) {
		
		this.idClase = idClase;
		this.idClase = idSala;
		
	}

	public int getIdClase() {
		return idClase;
	}

	public void setIdClase(int idClase) {
		this.idClase = idClase;
	}

	public int getIdSala() {
		return idSala;
	}

	public void setIdSala(int idSala) {
		this.idSala = idSala;
	}
	
}
